import 'package:flutter_modular/flutter_modular_test.dart';
import 'package:flutter_test/flutter_test.dart';

import 'package:flutter_pokedex/app/modules/home/poke_screen/poke_screen_controller.dart';
import 'package:flutter_pokedex/app/modules/home/poke_screen/poke_screen_module.dart';

void main() {
  initModule(PokeScreenModule());
  PokeScreenController pokescreen;
  
  setUp(() {
        pokescreen = PokeScreenModule.to.get<PokeScreenController>();
  });

  group('PokeScreenController Test', () {
      test("First Test", () {
        expect(pokescreen, isInstanceOf<PokeScreenController>());
      });

      test("Set Value", () {
        expect(pokescreen.value, equals(0));
        pokescreen.increment();
        expect(pokescreen.value, equals(1));
      });
  });
}
